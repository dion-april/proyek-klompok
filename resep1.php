<h2>Bahan dan bumbu cilok :</h2>
125 gram tepung aci (kanji/tapioka)<br>
125 gram tepung terigu <br>
200 ml air panas mendidih <br>
1/2 sdt gula pasir <br>
1/4 sdt merica <br>
1/2 sdt kaldu bubuk <br>
garam secukupnya <br>
2 siung bawang putih, haluskan <br>
2 batang daun bawang, daunnya saja diiris tipis <br>
air untuk merebus secukupnya <br> <br>
<h2>Saus bumbu kacang : </h2>
100 gram kacang tanah, goreng <br>
7 buah cabe rawit merah <br>
4 sdm gula merah <br>
2 siung bawang putih <br>
1 sdm air asam jawa <br>
200 ml air <br>
garam secukupnya <br>
2 sdm minyak untuk menumis <br>
minyak seperlunya untuk menggoreng <br> <br>
<h2>CARA MEMBUAT CILOK BANDUNG KENYAL DAN EMPUK BUMBU KACANG : </h2>
1. Siapkan wadah, masukkan tepung terigu dan irisan daun bawang kemudian tuang air panas yang sudah dicampur dengan bawang putih halus, garam, gula, merica dan kaldu bubuk. Aduk rata lalu masukkan tepung aci sedikit demi sedikit sampai adonan bisa dibentuk dan tidak lengket. <br> <br>
2. Bentuk bulat-bulat kecil adonan, rebus dalam air yang sudah mendidih hingga cilok mengapung pertanda cilok sudah matang. Angkat, tiriskan dari air dan cilok siap untuk disajikan dengan bumbu kacang serta tambahkan kecap manis bila suka. <br> <br>
3. Haluskan bahan-bahan untuk bumbu kacang kemudian tumis dengan sedikit minyak, beri garam secukupnya lalu aduk sampai matang. <br>
