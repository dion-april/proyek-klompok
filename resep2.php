air pandan (hasil blender beberapa helai daun pandan dan air), secukupnya <br>
gula aren, disisir secukupnya <br>
kelapa setengah tua secukupnya, kupas, parut, lalu kukus 20 menit
<br>
<h2>CARA MEMBUAT KLEPON :</h2>
1. Campur tepung beras, tepung ketan, garam dan air pandan hingga adonan bisa dibentuk. Jangan sampai terlalu lembek. <br>
2. Bentuk adonan jadi bulat-bulat, beri isian gula aren secukupnya. <br>
3. Rebus air hingga mendidih, masukkan bulatan klepon. <br>
4. Bila klepon sudah mengapung, angkat dan tiriskan. <br>
5. Sajikan dengan parutan kelapa. <br>
